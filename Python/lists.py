#Adding and removing lists

fruits = ["kiwi","apple","lemon",]
fruits.append("strawberry")

print(fruits)

fruits.insert(1,"orange")
print(fruits)

fruits.remove("strawberry")
print(fruits)

fruits.sort(reverse=True)
print(fruits)